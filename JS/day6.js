// 1: create & print object.

let person = {
    name: "MAdan.R",
    age: 22,
    city: "Bangalore"
};

console.log(person);

// 2: Access Object Values (Dot notation)

console.log("Name:", person.name);
console.log("Age:", person.age);
console.log("City:", person.city);

// 3: Access Object Values (Bracket notation)

console.log("Name:", person["name"]);
console.log("Age:", person["age"]);
console.log("City:", person["city"]);